package Model;

/**
 *
 * @author lecha
 */
public class Users {
    private String id;
    private String name;
    private String pass;
    private String mail;
    private String phone;
    private String role;

    public Users() {
    }

    public Users(String id, String name, String pass, String mail, String phone, String role) {
        this.id = id;
        this.name = name;
        this.pass = pass;
        this.mail = mail;
        this.phone = phone;
        this.role = role;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getMail() {
        return mail;
    }

    public void setMail(String mail) {
        this.mail = mail;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
    
    
}
